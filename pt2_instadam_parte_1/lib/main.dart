import 'package:flutter/material.dart';
import 'registroUsuarios.dart';
import 'loggingusuarios.dart';

void main() {
  runApp(MyApp());
}

class MyApp extends StatelessWidget {
  @override
  Widget build(BuildContext context) {

    return MaterialApp(
      home: HomePage(),
    );
  }
}

class HomePage extends StatelessWidget {
