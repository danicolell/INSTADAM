        title: Text('INSTADAM - Página de Inicio'),
      ),
      body: Center(
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: [
            ElevatedButton(
              onPressed: () {
                Navigator.push(
                  context,
                  MaterialPageRoute(builder: (context) => RegistroUsuarioScreen()),
                );
              },
              child: Text('Registrar Usuario'),
            ),
            SizedBox(height: 20),
            ElevatedButton(
              onPressed: () {
